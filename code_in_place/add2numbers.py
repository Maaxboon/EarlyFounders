#!/usr/bin/env python3
def main():
    print("This program adds two numbers.")

    # Asks user for the first number, the variable num1
    num1 = int(input("Enter first number: "))

    # Asks user for the second number, variable num2
    num2 = int(input("Enter the second number: "))


    # Adds num1 and num2 to find the total
    total = num1 + num2

    # Prints the sum using a formatted print function
    print(f"The total is {total}.")
main()